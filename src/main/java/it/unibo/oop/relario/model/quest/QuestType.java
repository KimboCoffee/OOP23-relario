package it.unibo.oop.relario.model.quest;

/**
 * Enumeration representing the quest types.
 */
public enum QuestType {
    /** Represent no quest. */
    NO_QUEST,

    /** The quest is to collect something. */
    COLLECTION_QUEST,

    /** The quest is to interact with some npc. */
    NPC_INTERACT_QUEST,

    /** The quest is to defeat some enemy. */
    DEFEAT_ENEMY_QUEST;
}
